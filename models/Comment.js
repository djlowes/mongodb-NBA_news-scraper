// ----------------------------------------------------------------------------------------------------------------------------
// Node Dependencies
// ----------------------------------------------------------------------------------------------------------------------------
var mongoose = require('mongoose');

// ----------------------------------------------------------------------------------------------------------------------------
// Create Schema
// ----------------------------------------------------------------------------------------------------------------------------
var Schema = mongoose.Schema;
var CommentSchema = new Schema({
  author: {
    type: String
  },
  content: {
    type: String
  }

});

// ----------------------------------------------------------------------------------------------------------------------------
// Comment Model
// ----------------------------------------------------------------------------------------------------------------------------
var Comment = mongoose.model('Comment', CommentSchema);


module.exports = Comment;
